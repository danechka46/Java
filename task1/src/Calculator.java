public class Calculator {
    /**
     * @param n1 the first number for calculate
     * @param n2 the second number for calculate
     * @return result of calculate
     */
    public double multiply(double n1, double n2) {
        return n1 * n2;
    }
    public double add(double n1, double n2) {
        return n1 + n2;
    }
    public double devide(double n1, double n2) {
        return n1 / n2;
    }
    public double minus(double n1, double n2) {
        return n1 - n2;
    }

}

